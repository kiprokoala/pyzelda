max = 0

for z in range(len(level)) :
    if level[z] > max :
        max = level[z]

tiles = []

for z in range(max + 1) :
    tiles.append(pygame.image.load(str(z) + ".png").convert())

player = [pygame.image.load("left.png").convert(), pygame.image.load("right.png").convert(), 0, 0, 1]
player[0].set_colorkey((0, 0, 0), RLEACCEL)
player[1].set_colorkey((0, 0, 0), RLEACCEL)

for z in range(len(level)) :
    if level[z] == 2 :
        player[2] = z % 64 * 64
        player[3] = int(z / 64) * 128 - 32

        level[z] = 0

keyboard = [False, False, False, False]

clock = pygame.time.Clock()

while True :
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            pygame.quit()

            exit()
        elif event.type == KEYDOWN :
            if event.key == K_LEFT :
                keyboard[0] = True
            elif event.key == K_RIGHT :
                keyboard[1] = True
            elif event.key == K_UP :
                keyboard[2] = True
            elif event.key == K_DOWN :
                keyboard[3] = True
        elif event.type == KEYUP :
            if event.key == K_LEFT :
                keyboard[0] = False
            elif event.key == K_RIGHT :
                keyboard[1] = False
            elif event.key == K_UP :
                keyboard[2] = False
            elif event.key == K_DOWN :
                keyboard[3] = False

    if keyboard[0] and level[int(player[3] / 64) * 32 + int((player[2] - 8) / 64)] == 0 and level[int(player[3] / 64) * 32 + int((player[2] - 8) / 64) + 32] == 0 and (level[int(player[3] / 64) * 32 + int((player[2] - 8) / 64) + 64] == 0 or player[3] % 64 == 0) :
        player[2] -= 8
        player[4] = 0
    elif keyboard[1] and level[int(player[3] / 64) * 32 + int(player[2] / 64) + 1] == 0 and level[int(player[3] / 64) * 32 + int(player[2] / 64) + 33] == 0 and (level[int(player[3] / 64) * 32 + int(player[2] / 64) + 65] == 0 or player[3] % 64 == 0) :
        player[2] += 8
        player[4] = 1

    if keyboard[2] and level[int((player[3] - 8) / 64) * 32 + int(player[2] / 64)] == 0 and (level[int((player[3] - 8) / 64) * 32 + int(player[2] / 64) + 1] == 0 or player[2] % 64 == 0) :
        player[3] -= 8
    elif keyboard[3] and level[int(player[3] / 64) * 32 + int(player[2] / 64) + 64] == 0 and (level[int(player[3] / 64) * 32 + int(player[2] / 64) + 65] == 0 or player[2] % 64 == 0) :
        player[3] += 8

    window.fill((0, 0, 0))

    for x in range(13) :
        for y in range(13) :
            z = (y + int((player[3] - 384) / 64)) * 32 + x + int((player[2] - 384) / 64)
            window.blit(tiles[level[z]], (x * 64 - player[2] % 64, y * 64 - player[3] % 64))

    sprite = int(player[2] / 64 + player[3] / 64) % 4

    if not keyboard[0] and not keyboard[1] and not keyboard[2] and not keyboard[3] :
        sprite = 0

    window.blit(player[player[4]], (384, 384), (sprite * 64, 0, 64, 128))

    pygame.display.flip()
    clock.tick(60)
